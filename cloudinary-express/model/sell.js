const mongoose = require('mongoose');

const sellSchema = new mongoose.Schema({
  productName: {
    type: String,
    required: true,
  },
  quantity: {
    type: Number,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
});

const sell = mongoose.model('sell', sellSchema);

module.exports = sell;
